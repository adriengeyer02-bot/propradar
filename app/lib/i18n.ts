export function toEnglishText(value: unknown): string {
  if (value === null || value === undefined) return '';
  const raw = String(value);
  const exact = exactTranslations[raw];
  if (exact) return exact;

  return phraseTranslations.reduce((text, [pattern, replacement]) => text.replace(pattern, replacement), raw);
}

export function toEnglishList(values: string[]) {
  return values.map((value) => toEnglishText(value));
}
